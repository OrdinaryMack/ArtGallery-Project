// eslint-disable-next-line no-unused-vars
function flipCard(button) {
  const card = button.closest('.over-card');
  card.classList.toggle('flip');
}
const artGrid = document.getElementById('artGrid');

const template = document.getElementById('artCardTemplate');

Tbl_Art.forEach((artwork) => {
  const cardClone = template.content.cloneNode(true);
  cardClone.querySelector('img').src = artwork.image;
  cardClone.querySelector('img').alt = artwork.ArtName;
  cardClone.querySelector('h3').textContent = artwork.ArtName;
  cardClone.querySelector('footer').textContent += artwork.owner;
  cardClone.querySelector('.art-id').textContent = artwork.ArtId;
  cardClone.querySelector('.art-description').textContent = artwork.ArtDescription;
  artGrid.appendChild(cardClone);
});